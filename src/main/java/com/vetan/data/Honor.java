package com.vetan.data;

public class Honor {

	private String scholar;
	  private String child;
	  private String relation;
	  private String year;
	  private String school;
	  private String application;
	  private String pass_year;
	  private String grade;
	  private String s_amount;
	  private String remarks;
	public String getScholar() {
		return scholar;
	}
	public void setScholar(String scholar) {
		this.scholar = scholar;
	}
	public String getChild() {
		return child;
	}
	public void setChild(String child) {
		this.child = child;
	}
	public String getRelation() {
		return relation;
	}
	public void setRelation(String relation) {
		this.relation = relation;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getSchool() {
		return school;
	}
	public void setSchool(String school) {
		this.school = school;
	}
	public String getApplication() {
		return application;
	}
	public void setApplication(String application) {
		this.application = application;
	}
	public String getPass_year() {
		return pass_year;
	}
	public void setPass_year(String pass_year) {
		this.pass_year = pass_year;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getS_amount() {
		return s_amount;
	}
	public void setS_amount(String s_amount) {
		this.s_amount = s_amount;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public Honor(String scholar, String child, String relation, String year, String school, String application,
			String pass_year, String grade, String s_amount, String remarks) {
		super();
		this.scholar = scholar;
		this.child = child;
		this.relation = relation;
		this.year = year;
		this.school = school;
		this.application = application;
		this.pass_year = pass_year;
		this.grade = grade;
		this.s_amount = s_amount;
		this.remarks = remarks;
	}
	  



}

	

